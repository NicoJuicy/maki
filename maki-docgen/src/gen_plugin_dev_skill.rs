/// Emit {content} as a Lua long-bracket string literal. The forced trailing
/// newline keeps content away from the closing delimiter, so scanning for
/// `]=]`-style brackets inside the content picks a safe level. Lua strips the
/// first newline after the opening bracket, so the content survives exactly.
fn lua_long_string(content: &str) -> String {
    let newline = if content.ends_with('\n') { "" } else { "\n" };
    let eq = (0..)
        .map(|n| "=".repeat(n))
        .find(|eq| !content.contains(&format!("]{eq}]")))
        .expect("unbounded range");
    format!("[{eq}[\n{content}{newline}]{eq}]")
}

/// Returns the skill module and the reference module. The skill carries the
/// guide, example, and a line-numbered index; the skill plugin writes the
/// full reference to disk so the model reads only the sections it needs.
pub fn generate() -> (String, String) {
    let reference = gen_lua_api::reference();
    let content = format!(
        "{GUIDE}{EXAMPLE}```\n\n# Full API reference\n\n\
         The complete Lua API reference - every function with parameters, return\n\
         values, and examples, plus shared helper module sources - is on disk at:\n\n\
         `{REFERENCE_PLACEHOLDER}`\n\n\
         The index below maps every function to its line in that file. Before using\n\
         a function you are not certain about, read its section (read tool with\n\
         offset = line number) or grep the file for its name. Never guess a\n\
         signature or parameter table from the index alone.\n\n\
         Signatures use Neovim notation: `{{path}}` is required, `{{opts?}}` is\n\
         optional, `{{...}}` is variadic.\n{}",
        gen_lua_api::reference_index(&reference)
    );
    // {:?} escapes are valid Lua for these ASCII consts; spec.lua loads the output in CI.
    let skill = format!(
        "-- Generated by maki-docgen. Do not edit; run `just gen-docs`.\nreturn {{\n  name = {NAME:?},\n  description = {DESCRIPTION:?},\n  reference_placeholder = {REFERENCE_PLACEHOLDER:?},\n  content = {},\n}}\n",
        lua_long_string(&content)
    );
    let reference_module = format!(
        "-- Generated by maki-docgen. Do not edit; run `just gen-docs`.\nreturn {{\n  content = {},\n}}\n",
        lua_long_string(&reference)
    );
    (skill, reference_module)
}

