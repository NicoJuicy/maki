mod tests {
    use super::lua_long_string;
    use test_case::test_case;

    #[test_case("no brackets\n", "[[\nno brackets\n]]"; "plain text level zero")]
    #[test_case("a ]] b\n", "[=[\na ]] b\n]=]"; "closing level zero taken")]
    #[test_case("a ]] b ]=] c\n", "[==[\na ]] b ]=] c\n]==]"; "levels zero and one taken")]
    #[test_case("ends with bracket eq ]=", "[[\nends with bracket eq ]=\n]]"; "delimiter prefix suffix gets newline")]
    #[test_case("no trailing newline", "[[\nno trailing newline\n]]"; "trailing newline added")]
    fn long_string_is_safe(content: &str, expected: &str) {
        assert_eq!(lua_long_string(content), expected);
    }
}
